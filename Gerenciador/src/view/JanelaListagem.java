    public JanelaListagem() {
        initComponents();

        for (Turma t : Principal.turmas) {
            String data[] = {"" + t.getId(), t.getNomeTurma(), t.getProfessor(), t.getTurno()};
            DefaultTableModel tb = (DefaultTableModel) tabelaTurmas.getModel();
            tb.addRow(data);
        }
        
        tabelaTurmas.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int row = tabelaTurmas.getSelectedRow(); // Obt√©m a linha selecionada
                if (row != -1) { // Verifica se uma linha foi selecionada
                    Object id = tabelaTurmas.getValueAt(row, 0); // Valor da coluna 0 (ID)
                    Object nome = tabelaTurmas.getValueAt(row, 1); // Valor da coluna 1 (NomeTurma)
                    Object professor = tabelaTurmas.getValueAt(row, 2); // Valor da coluna 2 (professor)
                    Object turno = tabelaTurmas.getValueAt(row, 3); // Valor da coluna 2 (turno)
                    Principal.turma.setId(Integer.parseInt("" + id));
                    Principal.turma.setNomeTurma("" + nome);
                    Principal.turma.setProfessor("" + professor);
                    Principal.turma.setTurno("" + turno);
                    JanelaPrincipal janelaPrincipal = new JanelaPrincipal();
                    janelaPrincipal.setVisible(true);
                    janelaPrincipal.setLocationRelativeTo(null);
                    dispose();
                }
            }
        });
        
    }

